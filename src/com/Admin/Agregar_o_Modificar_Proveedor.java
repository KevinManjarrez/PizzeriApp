    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Connection conexion = null;
        
        try{
            conexion = getConnection();
            ps = conexion.prepareStatement("insert into proveedores (idProveedores,NombreProveedores,DireccionProveedores,cpProveedores,telProveedores) values(?,?,?,?,?)");
            ps.setString(1,txtId.getText());
            ps.setString(2,txtNombre.getText());
            ps.setString(3,txtDireccion.getText());
            ps.setString(4,txtCP.getText());
            ps.setString(5,txtTel.getText());
            int resultado = ps.executeUpdate(); //Ejecutamos la insercion
            
            if(resultado > 0){ //Se ejecuto correctamente la insercion
                JOptionPane.showMessageDialog(null, "Registro agregado correctamente");
                limpiarCajas();
            }
            else{
                JOptionPane.showMessageDialog(null, "Error al agregar el registro");
                limpiarCajas();
            }
            
            conexion.close(); //Cerramos la conexion
            
        }catch(Exception ex){
            System.err.println("Error, "+ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

